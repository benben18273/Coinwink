            <h3 style="margin-top:-5px;">About Coinwink</h3>

            <p>
                Coinwink is a cryptocurrency price alerts, watchlist and portfolio tracking app for Bitcoin, Ethereum, XRP, and other 2500+ crypto coins and tokens.
            </p>

            <p>
                Coinwink monitors cryptocurrency prices 24/7 and alerts you by e-mail or SMS when your defined conditions are met.
            </p>

            <p>
                Additional tools, such as Portfolio and Watchlist allow you to track your crypto holdings and favorites from different blockchains in one single place. These tools help you to be aware of the market situation with the minimum amount of time and effort invested.
            </p>

            <p>
                Charts, social activity, links, news, and other additional information about any coin or a token are always just one click away, provided by <a href="https://coinmarketcap.com" class="blacklink" target="_blank">CoinMarketCap</a>.
            </p>

            <p>
                Coinwink's mission is to help people to develop well-balanced cryptocurrency management and trading habits, that provide a high level of personal freedom and positively impact investing outcomes.
            </p>
            
            <h3>History</h3>

            <p>
                Coinwink was founded in 2016. It was stress-tested, optimized, and hardened during the 2017's bull-run, and then polished and made feature-rich during 2018-2019.
            </p>

            <p>
                Since the founding, Coinwink is in a long-term professional partnership with CoinMarketCap, which is the main data provider for the cryptocurrency industry.
            </p>

            <p>
                Coinwink has thousands of active users and starting from 2016, around half of a million delivered alerts.
            </p>

            <p>
                In 2020, Coinwink continues to improve, grow, and to provide top-quality service to its users.
            </p>


            <h3>Features</h3>

            <p>
                Coinwink is a <a href="https://twitter.com/Coinwink/status/1164883857419788289" target="_blank" class="blacklink">cross-platform</a> web app and works on any desktop or a mobile device.
            </p>

            <p>
                Coinwink is a <a href="https://coinwink.com/privacy" target="_blank" class="blacklink">privacy-focused</a> service. The data of our users is never shared with third parties. Our users do not receive spam.
            </p>

            <p>
                Coinwink is <a href="https://github.com/coinwink/Coinwink" target="_blank" class="blacklink">open-source</a>.
            </p>

            <p>
                Coinwink is fast. Depending on the location, the app <a href="https://twitter.com/Coinwink/status/962879291741519872" target="_blank" class="blacklink">loads</a> in less than 1 second. A new visitor can create an alert in seconds, even without the account. Alerts are activated every 1-2 minutes.
            </p>

            <p>
                Below, the main three Coinwink features are explained in detail.
            </p>

            <h1>Crypto Alerts</h1>

            <p>Coinwink started as a crypto alerting service, and this is what it does best.</p>

            <p>When you create a Coinwink crypto alert, you can safely forget charts because Coinwink is now watching the price for you.</p>

            <p>The two delivery methods - SMS and email - is the universal, timeless and customizable combination that works for all possible scenarios.</p>

            <p>Coinwink is not relying on mobile notifications since they are inherently annoying, easy to miss and often decrease productivity.</p>

            <p>Coinwink crypto alerts are reliable, precise and fast.</p>

            <h1>Crypto Portfolio</h1>

            <p>Coinwink Portfolio allows you to keep track of your crypto holdings in a single structured dashboard.</p>

            <p>You can convert between different currencies, calculate return on investment (ROI), make notes for individual coins, and get new insights into the overall situation of the portfolio.</p>

            <p>Additionally, the portfolio has a multiple-coin crypto alerts feature, introduced in 2019.</p>

            <p>You only need to set multiple-coin alerts once. After that, there is no more need to keep checking the portfolio until any coin in the portfolio passes the threshold, and the alert is received.</p>

            <h1>Crypto Watchlist</h1>

            <p>
                Coinwink Watchlist is a newly released feature. Its UI is consistent with the portfolio, which makes both tools easy to use.
            </p>
            
            <p>
                Apart from navigational similarities, the watchlist serves a completely different function than the portfolio.
            </p>
            
            <p>
                Watchlist is for the opportunity hunt. Also, it is a way to get out of a “filter bubble”.
            </p>

            <h3>Tips & Tricks</h3>

            <p>
                You can place the Coinwink app icon on your mobile device home screen. To do this, open coinwink.com in your mobile Chrome browser, click settings and then Add to Home Screen. You 
                will then be able to use Coinwink similarly as a mobile app. See a how-to <a href="https://twitter.com/Coinwink/status/1164883857419788289" class="blacklink" target="_blank">video</a>.
            </p>

            <p>
                Use (and bookmark) a custom url address to pre-select your coin, e.g. <a href="https://coinwink.com/eth" class="blacklink" target="_blank">https://coinwink.com/eth</a> (put a coin symbol after the slash).
            </p>

            <p>When creating a new alert, click the USD value to switch between currencies.</p>

            <p>In your Manage Alerts view, click an alert to enable/disable it.</p>

            <p>Track your activated alerts with Logs in your Manage Alerts view.</p>

            <p>For faster navigation in portfolio and watchlist, use keyboard shortcuts (Enter, Tab, Shift+Tab).</p>

            <p>In the watchlist, click the price column to switch between volume and market cap.</p>

            <p>On modern browsers, drag and drop to re-order watchlist coins.</p>

            <p>Use the portfolio alerts feature to track multiple coins at once.</p>

            <p>
                Click on any coin's logo, anywhere inside of the Coinwink app, to open that coin's CoinMarketCap page, where you will find additional data: charts, social news, stats, and other details about that coin.
            </p>
            
            <h3>Philosophy</h3>

            <p>Coinwink is designed around the philosophy described in the book Trading in the Zone, written by Mark Douglas.</p>

            <p>It states that trading is successful when it's done from the carefree, winning mindset, which is based not on extensive analysis, but on cultivated intuition.</p>

            <p>After all, it only takes one wink.</p>

            <p>Additionally, Coinwink app is designed according to the Pareto principle. That is why only the most important information (20%) is presented while trying to avoid the rest 80%, which is less important and can be overwhelming.</p>

            <p>Coinwink also employs the "permanent beta" philosophy, which allows us to always learn and continuously improve.</p>

